    COLOR_BUTTON_DANGER_HOVER_BACKGROUND  = UTOX_RGB(dc, 56, 56);
    COLOR_BUTTON_DANGER_HOVER_TEXT        = COLOR_MAIN_BACKGROUND;
    COLOR_BUTTON_DISABLED_BACKGROUND      = UTOX_RGB(d1, d1, d1);
