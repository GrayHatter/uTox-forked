    COLOR_ACTIVEOPTION_TEXT       = COLOR_MAIN_TEXT;

    //---- Status circles ----
    COLOR_STATUS_ONLINE = UTOX_RGB(6b, c2, 60);
    COLOR_STATUS_AWAY   = UTOX_RGB(ce, bf, 45);
    COLOR_STATUS_BUSY   = UTOX_RGB(c8, 4e, 4e);

