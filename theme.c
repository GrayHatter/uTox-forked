        COLOR_MAIN_SUBTEXT = UTOX_RGB(bb, bb, bb);
        COLOR_MAIN_ACTIONTEXT = UTOX_RGB(27, a9, bc);
        COLOR_MAIN_URLTEXT = COLOR_MAIN_ACTIONTEXT;
        COLOR_MAIN_QUOTETEXT = UTOX_RGB(55, b3, 17);
