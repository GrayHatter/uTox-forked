        COLOR_EDGE_HOVER = UTOX_RGB(70, 70, 70);
        COLOR_ACTIVEOPTION_BACKGROUND = UTOX_RGB(DD, EE, FF);
        COLOR_ACTIVEOPTION_TEXT = COLOR_MAIN_TEXT;
        break;

    case THEME_HIGHCONTRAST:

        COLOR_MAIN_BACKGROUND = UTOX_RGB(FF, FF, FF);
        COLOR_MAIN_TEXT = UTOX_RGB(00, 00, 01);
